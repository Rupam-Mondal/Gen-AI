#Basic for loop
fruits = ['apple', 'banana', 'cherry']
for fruit in fruits:
    print(fruit)

# Loop through a sequence of numbers from 0 to 4
for i in range(5):
    print(i)

# Loop through a sequence of numbers with a start and end
for i in range(1, 6):
    print(i)

# Loop through a sequence with a step value
for i in range(1, 10, 2):
    print(i)
